public class Vector3Float {
    
    public float x, y, z;
    
    //Constructor: Vector3Float(int, int, int)
    //Purpose: Keep track of camera position in 3D space
    Vector3Float(int x, int y, int z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
